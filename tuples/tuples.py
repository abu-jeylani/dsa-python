# Time complexity: O(1)
# space complexity: O(n)



new_tuple = 'a', 'b', 'c', 'd', 'e'

print(new_tuple)

# or
new_tuple2 = ('a', 'b', 'c', 'd', 'e')

print(new_tuple2)

# for single items, always put a comma at the end

not_a_tuple = ('a')
print(not_a_tuple)
a_tuple = ('a',)
print(a_tuple)

