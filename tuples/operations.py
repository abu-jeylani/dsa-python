my_tuple = (1,4,3,2,5)
my_tuple1 = (1,2,6,9,8,7)

print(my_tuple + my_tuple1) # combines tuples

print(my_tuple*4) # duplicates tuple x times

print(4 in my_tuple) # returns true

print(my_tuple1.count(2)) # counts how many times a value appears

print(my_tuple1.index(4)) # returns the index of the value

print(max(my_tuple1)) # returns the max

print(min(my_tuple1)) # returns the min

print(tuple([1,2,3,4])) #converts list to tuple